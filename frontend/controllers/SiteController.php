    {
        $id = Yii::$app->getRequest()->get('id');
        $name = 'testName';
        $testModel = new Test();
        $rs = $testModel->sendTestCurl(['id' => $id, 'name' => $name]);
        

        return $this->renderContent($id);
    }


}
